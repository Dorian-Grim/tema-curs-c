#include <stdio.h>

int main()
{
    int sum = 0;
    int tmp = 0;
    int cnt = 1;
    float avg;

    printf("\nNumarul -99 opreste secventa\n");
    while (tmp != -99)
    {
        printf("suma este: %i\n", sum);
        printf("media este: %f\n", avg);
        printf("Numarul nr. %i\n", cnt);
        scanf("%d", &tmp);
        cnt++;

        sum += tmp;
        avg = (float)sum / cnt;
    }
    printf("\n");

    return 0;
}

