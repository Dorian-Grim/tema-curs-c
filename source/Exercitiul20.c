#include <stdio.h>

int main()
{
  int a, b, tmp;

  a = 11;
  b = 9;

  printf("Cele doua numere sunt: \n a = %d, b = %d \n\n", a, b);

  tmp = (a + b) + (a - b);

  printf("tmp este: \n tmp = %d \n", tmp);
}

/*
-----output------

Cele doua numere sunt:
 a = 11, b = 9

tmp este:
 tmp = 22

-------------------
*/
